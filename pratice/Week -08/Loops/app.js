// While Loop

let i = 0;

while (i < 10) {
    console.log("while", i);
    i = i + 1;
}


// Do while Loop
// It always  runs atleast 1 time then check the condition

let a = 0;
do {
    console.log("do-while ", a)
    a = a + 1;
} while (a < 10);

// For Loop 

for (let b = 0; b < 10; b++) {
    console.log("For", b)
}